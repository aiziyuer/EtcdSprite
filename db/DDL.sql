-- 创建隧道信息
DROP TABLE IF EXISTS TUNNEL_INFO;
CREATE TABLE TUNNEL_INFO (
  ID                 NUMBER,
  ALIAS				 VARCHAR2,
  LOCAL_TUNNEL_HOST  VARCHAR2,
  LOCAL_TUNNEL_PORT  NUMBER,
  IS_LOCAL           BOOLEAN,
  REMOTE_TUNNEL_HOST VARCHAR2,
  REMOTE_TUNNEL_PORT NUMBER,
  SESSION_INFO_ID    NUMBER
);

