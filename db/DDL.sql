-- 创建SSH的Session信息的表
DROP TABLE IF EXISTS SESSION_INFO;
CREATE TABLE SESSION_INFO (
  ID                 NUMBER,
  HOST               VARCHAR2,
  PORT               NUMBER,
  NAME               VARCHAR2,
  PASSWORD           VARCHAR2
);
