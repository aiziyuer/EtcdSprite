-- 创建SSH的Session信息的表
DROP TABLE IF EXISTS SESSION_INFO;
CREATE TABLE SESSION_INFO (
  ID                 NUMBER,
  HOST               VARCHAR2,
  NAME               VARCHAR2,
  PASSWORD           VARCHAR2,
  ENTRY_TUNNEL_PORT  NUMBER,
  EXPORT_TUNNEL_HOST VARCHAR2,
  EXPORT_TUNNEL_PORT NUMBER
);
